DROP TABLE IF EXISTS AUTHOR;
DROP TABLE IF EXISTS GENRE;
DROP TABLE IF EXISTS BOOK;
DROP TABLE IF EXISTS BOOK_AUTHOR;
DROP TABLE IF EXISTS BOOK_GENRE;
CREATE TABLE AUTHOR(ID BIGINT PRIMARY KEY auto_increment, FIRST_NAME VARCHAR(255), MIDDLE_NAME VARCHAR (255), LAST_NAME VARCHAR(255));
CREATE TABLE GENRE(ID BIGINT PRIMARY KEY auto_increment, TITLE VARCHAR(255));
CREATE TABLE BOOK(ID BIGINT PRIMARY KEY auto_increment, TITLE VARCHAR(1024));
CREATE TABLE BOOK_AUTHOR(
    ID BIGINT PRIMARY KEY auto_increment,
    BOOK_ID BIGINT,
    AUTHOR_ID BIGINT,
    foreign key (BOOK_ID) references book(ID),
    foreign key (AUTHOR_ID) references AUTHOR(ID) );
CREATE TABLE BOOK_GENRE(
    ID BIGINT PRIMARY KEY auto_increment,
    BOOK_ID BIGINT,
    GENRE_ID BIGINT,
    foreign key (BOOK_ID) references book(ID),
    foreign key (GENRE_ID) references GENRE(ID) );
